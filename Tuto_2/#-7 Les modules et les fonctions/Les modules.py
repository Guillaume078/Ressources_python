# le module Random
import random

# la fonction randint
r = random.randint(0, 1) #la valeur est inclusive
print(r)

# la fonction uniform

r = random.uniform(0,1)
print(r)

# la fonction randrange

r =  random.randrange(2) #la valeur est exclusive (randrange(0, 3, 15) avec trois comme pas)
print (r)